/* Private variables ---------------------------------------------------------*/
TIM_HandleTypeDef htim1;
TIM_HandleTypeDef htim2;
TIM_HandleTypeDef htim3;
TIM_HandleTypeDef htim4;
UART_HandleTypeDef huart1;
DMA_HandleTypeDef hdma_usart1_rx;
DMA_HandleTypeDef hdma_usart1_tx;

/* UART SBSP related */
bool frame_received = false;
uint16_t frame_len;

/* Buffer used for reception */
uint8_t RxBuffer[RXBUFFERSIZE]; // UART RX DMA
uint8_t FrameBuffer[RXBUFFERSIZE]; // frame processing buffer

/* PPM */
// four PPM signals
PPM_Signal_t PPM_Signal[4];

/* Private function prototypes */
void SystemClock_Config(void);
static void MX_GPIO_Init(void);
static void MX_DMA_Init(void);
static void MX_USART1_UART_Init(void);
static void MX_TIM1_Init(void);
static void MX_TIM2_Init(void);
static void MX_TIM3_Init(void);
static void MX_TIM4_Init(void);

static void PPM_Updating(void);

int main(void)
{
    /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
    HAL_Init();

    /* Configure the system clock to 72 MHz */
    SystemClock_Config();

    /* Initialize all configured peripherals */
    MX_GPIO_Init();
    MX_DMA_Init();
    MX_USART1_UART_Init();
    MX_TIM1_Init();
    MX_TIM2_Init();
    MX_TIM3_Init();
    MX_TIM4_Init();

    /* Init PPM signal states */
    for (uint8_t i = 0; i < 4; i++)
        PPM_Signal_Init(&PPM_Signal[i]);

    /* Start producing PPM signals */
    HAL_TIM_Base_Start_IT(&htim1);
    HAL_TIM_Base_Start_IT(&htim2);
    HAL_TIM_Base_Start_IT(&htim3);
    HAL_TIM_Base_Start_IT(&htim4);

    /* enable idle interrupt of UART1 */
    __HAL_UART_ENABLE_IT(&huart1, UART_IT_IDLE);
    /* start UART DMA receiving process */
    HAL_UART_Receive_DMA(&huart1, RxBuffer, RXBUFFERSIZE); // enable dma receiving

    /* Infinite loop */
    while (1)
    {
        if (frame_received == true) // if received a frame
        {// process this frame

            // parse this frame
            if (sppFrameParsing(FrameBuffer, frame_len))
                // update PPM_Channel_Value
                PPM_Updating();

            // clear flag
            frame_received = false; 
        }
    }

    return 0;
}

static void PPM_Updating(void)
{
    memcpy(PPM_Signal);
}

